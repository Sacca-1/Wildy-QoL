	static boolean isTeleport(int itemId, TeleportOutWarningMode mode)
	{
		if (mode == TeleportOutWarningMode.LEVEL_30)
		{
			return LEVEL_30_TELEPORT_ITEMS.contains(itemId);
		}

		return mode == TeleportOutWarningMode.LEVEL_20
			&& (LEVEL_20_TELEPORT_ITEMS.contains(itemId) || LEVEL_30_TELEPORT_ITEMS.contains(itemId));
	}
